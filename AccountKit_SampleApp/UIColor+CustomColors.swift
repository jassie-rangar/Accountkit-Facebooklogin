import Foundation
import UIKit

extension UIColor {
    
    static func seaGreen() -> UIColor {
        return UIColor(colorLiteralRed: 9/255, green: 212/255, blue: 182/255, alpha: 1.0)
    }
    
    static func lightSeaGreen() -> UIColor {
        return UIColor(colorLiteralRed: 9/255, green: 212/255, blue: 182/255, alpha: 0.5)
    }
    
    static func lightGray() -> UIColor {
        return UIColor(colorLiteralRed: 193/255, green: 205/255, blue: 216/255, alpha: 1.0)
    }
    
    static func mediumGray() -> UIColor {
        return UIColor(colorLiteralRed: 237/255, green: 242/255, blue: 246/255, alpha: 1.0)
    }
    
    static func blueGray() -> UIColor {
        return UIColor(colorLiteralRed: 125/255, green: 151/255, blue: 173/255, alpha: 1.0)
    }
    
    static func orange() -> UIColor {
        return UIColor(colorLiteralRed: 252/255, green: 98/255, blue: 64/255, alpha: 1.0)
    }
    
}



